package com.withiter.dto;

/**
 * @author cross
 *
 */
public class ResolutionDTO {

	private int x;
	private int y;
	private int width;
	private int height;

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

	public int getWidth() {
		return width;
	}

	public void setWidth(int width) {
		this.width = width;
	}

	public int getHeight() {
		return height;
	}

	public ResolutionDTO() {
		super();
	}

	public void setHeight(int height) {
		this.height = height;
	}



	public ResolutionDTO(int x, int y, int width, int height) {
		super();
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
	}
}
